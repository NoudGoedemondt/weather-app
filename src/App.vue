<template>
  <div>
    <h1>Weather Data</h1>
    <pre>{{ weatherData }}</pre>
  </div>
</template>

<script setup>
import { computed, onMounted } from 'vue';
import { useStore } from 'vuex';

const store = useStore();

const weatherData = computed(() => store.getters['weather/weatherData']);

onMounted(async () => {
  await store.dispatch('weather/fetchWeatherWithGeolocation');
});
</script>

